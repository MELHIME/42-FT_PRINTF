#include "ft_printf.h"

int	ft_put_s(char *s)
{
	int	i;

	if (!s)
	{
		ft_put_s("(null)");
		return (6);
	}
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
